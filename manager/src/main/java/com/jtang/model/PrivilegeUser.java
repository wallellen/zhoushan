package com.jtang.model;

public class PrivilegeUser {
	private String privilegeName;
	private String privilegeUser;
	/**
	 * @return the privilegeName
	 */
	public String getPrivilegeName() {
		return privilegeName;
	}
	/**
	 * @param privilegeName the privilegeName to set
	 */
	public void setPrivilegeName(String privilegeName) {
		this.privilegeName = privilegeName;
	}
	/**
	 * @return the privilegeUser
	 */
	public String getPrivilegeUser() {
		return privilegeUser;
	}
	/**
	 * @param privilegeUser the privilegeUser to set
	 */
	public void setPrivilegeUser(String privilegeUser) {
		this.privilegeUser = privilegeUser;
	}
	
	
}
